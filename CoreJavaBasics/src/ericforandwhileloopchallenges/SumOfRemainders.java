package ericforandwhileloopchallenges;

public class SumOfRemainders {

	public static void main(String[] args) {
	
		// write a for loop that calculates the sum of the remainders 
		//of each number divided by 2 between 1 and 100
		
		int sum = 0;
		for (int i = 1; i< 100; i++) sum +=i;
		System.out.println("Ths sum is " + sum);

	}

}
